export type TranslationKey = keyof typeof translations.fr;

export function getT(lang: Lang) {
  return (key: TranslationKey): string => translations[lang][key] ?? key;
}

export function timeAgo(dateStr: string, lang: Lang = "fr"): string {
  const diff = Date.now() - new Date(dateStr).getTime();
  const m = Math.floor(diff / 60000);
  if (m < 1) return lang === "en" ? "Just now" : "À l'instant";
  if (m < 60) return lang === "en" ? `${m} min ago` : `Il y a ${m} min`;
  const h = Math.floor(m / 60);
  if (h < 24) return lang === "en" ? `${h}h ago` : `Il y a ${h}h`;
  return lang === "en" ? `${Math.floor(h / 24)}d ago` : `Il y a ${Math.floor(h / 24)}j`;
}

